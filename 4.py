import numpy as np

# 根據 P(x) = 2x^5 + 4x^4 - 2x^3 - 4x^2 - 10x + 10
# 
# 按照「您」的定義 (c[i] 為 x^i 係數，升冪)：
# c[0]=10, c[1]=-10, c[2]=-4, c[3]=-2, c[4]=4, c[5]=2
c_user_order = [10, -10, -4, -2, 4, 2]

# 按照「NumPy」的要求 (降冪)：
# c_n, c_{n-1}, ..., c_0
c_numpy_order = [2, 4, -2, -4, -10, 10]

# ---------------------------------------------------
# 實際求解
# ---------------------------------------------------

# 方法一：直接手動輸入 NumPy 格式
roots_1 = np.roots(c_numpy_order)
print("方法一的解:\n", roots_1)

# 方法二：從您的格式轉換
# 我們可以使用 [::-1] 語法來反轉陣列
roots_2 = np.roots(c_user_order[::-1])
print("\n方法二的解 (與方法一相同):\n", roots_2)
